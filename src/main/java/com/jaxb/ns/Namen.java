public class Namen {
	private String Kort;

	public String getKort() {
		return this.Kort;
	}

	public void setKort(String Kort) {
		this.Kort = Kort;
	}

	private String Middel;

	public String getMiddel() {
		return this.Middel;
	}

	public void setMiddel(String Middel) {
		this.Middel = Middel;
	}

	private String Lang;

	public String getLang() {
		return this.Lang;
	}

	public void setLang(String Lang) {
		this.Lang = Lang;
	}
}

